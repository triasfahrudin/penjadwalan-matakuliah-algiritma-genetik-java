    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (("".equals(txtKode.getText().trim()) || "".equals(txtNIDN.getText().trim()))
                || "".equals(txtNama.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Data Belum Lengkap");
            return;
        } 

        if (_selectedkode != -1) {
            //update data
            String check = String.format(
                    "SELECT CAST(COUNT(*) AS CHAR(1)) "
                    + "FROM dosen "
                    + "WHERE (kode= %d OR nidn= '%s') AND kode <> %d",
                    Integer.parseInt(txtKode.getText()), txtNama.getText(), _selectedkode);
            try {
                int i = Integer.parseInt(MysqlConnect.getDbCon().ExecuteScalar(check));

                if (i != 0) {
                    JOptionPane.showMessageDialog(null, "Kode Atau NIDN ini sudah ada!");
                    return;
                }

                String Qry_update = String.format(
                        "UPDATE dosen "
                        + "SET kode = %d,"
                        + "    nidn = '%s', "
                        + "    nama = '%s', "
                        + "    alamat = '%s', "
                        + "    telp = '%s' "
                        + "WHERE kode = %d",
                        Integer.parseInt(txtKode.getText()),
                        txtNIDN.getText(),
                        txtNama.getText(),
                        txtAlamat.getText(),
                        txtTelp.getText(),
                        _selectedkode
                );
                MysqlConnect.getDbCon().insert(Qry_update);

                //update waktu_tidak_bersedia
                Qry_update = String.format(
                        "UPDATE waktu_tidak_bersedia "
                        + "SET kode_dosen = %d "
                        + "WHERE kode_dosen = %d",
                        Integer.parseInt(txtKode.getText()),
                        _selectedkode
                );
                MysqlConnect.getDbCon().insert(Qry_update);

            } catch (SQLException ex) {
                Logger.getLogger(FrmPengampu.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            //new data
            String check = String.format(
                    "SELECT CAST(COUNT(*) AS CHAR(1)) "
                    + "FROM dosen "
                    + "WHERE (kode= %d OR nidn= '%s')",
                    Integer.parseInt(txtKode.getText()), txtNama.getText());

            try {
                int i = Integer.parseInt(MysqlConnect.getDbCon().ExecuteScalar(check));

                if (i != 0) {
                    JOptionPane.showMessageDialog(null, "Kode Atau NIDN ini sudah ada!");
                    return;
                }

                String Qry_insert = String.format(
                        "INSERT INTO dosen(kode,nidn,nama,alamat,telp) VALUES(%d,'%s','%s','%s','%s')",
                        Integer.parseInt(txtKode.getText()), txtNIDN.getText(),
                        txtNama.getText(), txtAlamat.getText(),
                        txtTelp.getText());
                MysqlConnect.getDbCon().insert(Qry_insert);

            } catch (SQLException ex) {
                Logger.getLogger(FrmPengampu.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        _selectedkode = -1; //set to "-1" agar disign sebagai databaru
        txtKode.setText("");
        txtNIDN.setText("");
        txtNama.setText("");
        txtAlamat.setText("");
        txtTelp.setText("");

        setButtonStatus(true, false, false);

        try {
            loadData();
        } catch (SQLException ex) {
            Logger.getLogger(FrmPengampu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

