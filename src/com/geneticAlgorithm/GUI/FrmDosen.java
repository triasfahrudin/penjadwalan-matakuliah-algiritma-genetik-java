    private void loadData() throws SQLException {
        String query
                = "SELECT kode,"
                + "       nidn as NIDN,"
                + "       nama as Nama,"
                + "       alamat as Alamat,"
                + "       telp as Telp "
                + "FROM dosen "
                + "ORDER BY kode";
        ResultSet rs = MysqlConnect.getDbCon().ExecuteQuery(query);
        jTable1.setModel(util.buildTableModel(rs));
    }

